reamur     = float (input("masukan suhu dalam reamur  :"))
celcius    = 0.0
fahrenheit = 0.0
kelvin     = 0.0

celcius    = 5/4 * reamur
fahrenheit = 9/4 * reamur + 32 
kelvin     = 5/4 * reamur + 273

print ("suhu celcius    : %2.f"% celcius)
print ("suhu fahrenheit : %2.f"%fahrenheit)
print ("suhu kelvin     : %2.f"%kelvin)